from collections import defaultdict
from random import choice, randint, random

class Question:
    seen = defaultdict(int)
    total = 0
    size = len(lst)

    def render(self):
        i,w,o1,o2,o3 = self.generate()
        return str(i), (
            ' {} -\n'
            ' 1) {}\n'
            ' 2) {}\n'
            ' 3) {}'
        ).format(w,o1,o2,o3)

    def generate(self):
        for _ in range(100):
            tr, w, x = choice(lst)
            if tr.startswith('1') and random()>0.05:
                continue
            if random()>x**2:
                continue
            if self.seen[w]==0 or self.total >= self.size:
                break
        o2,o3 = choice(en_nouns), choice(en_nouns)
        i = randint(1,3)
        self.total += 1
        self.seen[w] += 1
        if tr.startswith('1'):
            tr = tr.split()[1]
        if i==1:
            return (i,w,tr,o2,o3)
        elif i==2:
            return (i,w,o2,tr,o3)
        else:
            return (i,w,o2,o3,tr)

